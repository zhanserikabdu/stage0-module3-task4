public class IntRepresentation {
    public static void main(String[] args) {
        int a = 99;
        char c = (char) a;
        System.out.println(c);
        int b = 104;
        char d = (char) b;
        System.out.println(d);
        int e = 97;
        char f = (char) e;
        System.out.println(f);
        int g = 114;
        char k = (char) g;
        System.out.println(k);
    }
}
